# main
.main:
.export main
push rb # store caller rb
push ra # store caller ra
sub sp, 0x05 # stack entry
# sp + 7 = __return_addr_0
# sp + 6 = __saved_rb_0
# sp + 5 = __saved_ra_0
# sp + 4 = width_1
# sp + 3 = size_1
# sp + 2 = count_1
# sp + 0 = pointer_inc_amount_1
# sp + 1 = i_2
loadi ra, 0x04 # load const value: 4
loada .get_value # function call
call .get_value
loada .main_end
jmp .main_end # return <exp>

.main_end:
add sp, 0x05 # stack exit
# locals size: 5 temp size: 0
add sp, 1 # drop ra because we're returning a value
pop rb # restore caller rb
ret

